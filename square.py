
def area(a):
    '''
        Возвращает площадь квадрата по заданной стороне

        Параметр: a (int): размер стороны квадрата
        Возвращаемое значение: a * a (int): площадь квадрата
    '''
    return a * a


def perimeter(a):
    '''
        Возвращает периметр квадрата по заданной стороне

        Параметр: a (int): размер стороны квадрата
        Возвращаемое значение: 4 * a (int): периметр квадрата
    '''
    return 4 * a
